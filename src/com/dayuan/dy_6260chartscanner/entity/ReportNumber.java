package com.dayuan.dy_6260chartscanner.entity;


public class ReportNumber {
	private int id;
	private int number;

	public ReportNumber() {
		super();
	}

	public ReportNumber(int id, int number) {
		super();
		this.id = id;
		this.number = number;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	};

}
